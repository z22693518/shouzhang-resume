function addLazyLoadingToImages() {
    // Âè™ËôïÁêÜÈùûÈóúÈçµÂúñÁâá
    const images = document.querySelectorAll('img:not([loading])');
    images.forEach(img => {
        // Ë∑≥ÈÅéÈóúÈçµÂúñÁâá
        if (!img.src.includes('logo') && !img.closest('.hero-image') && !img.closest('.nav-brand')) {
            img.setAttribute('loading', 'lazy');
        }
    });
}


// Video Background Initialization - ÂÑ™ÂåñËºâÂÖ•ÈÄüÂ∫¶
function initializeVideoBackground() {
    const video = document.getElementById('bgVideo');
    const videoSource = document.getElementById('videoSource');
    const fallbackBg = document.getElementById('fallbackBg');
    
    // ÂÖàÈ°ØÁ§∫ÂÇôÁî®ËÉåÊôØÔºåÈÅøÂÖçËºâÂÖ•Âª∂ÈÅ≤
    if (fallbackBg) fallbackBg.style.display = 'block';
    
    // Ê™¢Êü•ÂΩ±Áâá‰æÜÊ∫ê
    if (!videoSource.src || videoSource.src === '' || !videoSource.src.includes('background-video.mp4')) {
        showFallbackBackground();
        return;
    }
    
    // Âª∂ÈÅ≤ËºâÂÖ•ÂΩ±ÁâáÔºå‰∏çÈòªÂ°ûÂàùÂßãËºâÂÖ•
    setTimeout(() => {
        // ËôïÁêÜÂΩ±ÁâáËºâÂÖ•ÊàêÂäü
        video.addEventListener('loadeddata', () => {
            video.style.display = 'block';
            if (fallbackBg) fallbackBg.style.display = 'none';
        });
        
        // ËôïÁêÜÂΩ±ÁâáËºâÂÖ•Â§±Êïó
        video.addEventListener('error', () => {
            showFallbackBackground();
        });
        
        // Á¢∫‰øùÂΩ±ÁâáÂæ™Áí∞Êí≠Êîæ
        video.addEventListener('ended', () => {
            video.currentTime = 0;
            video.play();
        });
        
        // ÂòóË©¶Êí≠ÊîæÂΩ±Áâá
        video.play().catch(e => {
            showFallbackBackground();
        });
    }, 500); // Âª∂ÈÅ≤ 500ms ËºâÂÖ•ÂΩ±Áâá
}

function showFallbackBackground() {
    const video = document.getElementById('bgVideo');
    const fallbackBg = document.getElementById('fallbackBg');
    
    if (video) video.style.display = 'none';
    if (fallbackBg) fallbackBg.style.display = 'block';
}

// Êõ¥Êñ∞ÂΩ±Áâá URL ÁöÑÂáΩÊï∏
function setVideoBackground(videoUrl) {
    const videoSource = document.getElementById('videoSource');
    const video = document.getElementById('bgVideo');
    
    if (videoSource && video) {
        videoSource.src = videoUrl;
        video.load();
        initializeVideoBackground();
    }
}

// Skill bars animation
function initializeSkillBars() {
    const skillBars = document.querySelectorAll('.skill-progress');
    
    const observerOptions = {
        threshold: 0.5,
        rootMargin: '0px 0px -100px 0px'
    };
    
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                const skillBar = entry.target;
                const width = skillBar.getAttribute('data-width');
                
                setTimeout(() => {
                    skillBar.style.width = width;
                }, 200);
                
                observer.unobserve(skillBar);
            }
        });
    }, observerOptions);
    
    skillBars.forEach(bar => {
        bar.style.width = '0%';
        observer.observe(bar);
    });
}

